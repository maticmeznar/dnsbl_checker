func parseCVS() []*ListItem {
	lists := []*ListItem{}
	r := csv.NewReader(strings.NewReader(csvList))

ListLoop:
	for {
		record, err := r.Read()
		if err == io.EOF {
			break
		}
		if err != nil {
			log.Fatal(err)
		}

		item := &ListItem{
			Name:    record[1],
			Address: record[2],
		}

		if record[3] == "ipv4" {
			item.IP4 = true
		}

		if record[4] == "ipv6" {
			item.IP6 = true
		}

		if record[5] == "dom" {
			item.Domain = true
		}

		if record[6] == "b" {
			item.Blacklist = true
		}

		if record[6] == "w" {
			item.Whitelist = true
		}

		// if it's neither a whitelist nor a blacklist, skip it
		if !item.Blacklist && !item.Whitelist {
			continue
		}

		// remove private DNSBLs that don't work for public
		for _, v := range privateLists {
			if item.Address == v {
				continue ListLoop
			}
		}

		// remove broken DNSBLs that don't respond
		for _, v := range brokenLists {
			if item.Address == v {
				continue ListLoop
			}
		}

		lists = append(lists, item)

	}

	return lists
}

